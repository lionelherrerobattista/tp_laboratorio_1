#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED


/** \brief La función recibe dos números, los suma y muestra el resultado.
 *
 * \param float primer número.
 * \param float segundo número.
 * \return void
 *
 */
void sumar(float, float);


/** \brief La función recibe dos números, los resta y muestra el resultado.
 *
 * \param float primer número.
 * \param float segundo número.
 * \return void
 *
 */
void restar(float, float);



/** \brief La función recibe dos numeros, los multiplica y muestra el resultado.
 *
 * \param float primer número.
 * \param float segundo número.
 * \return void
 *
 */
void multiplicar(float, float);



/** \brief La función recibe dos numeros, los divide y muestra el resultado.
 *
 * \param float dividendo.
 * \param float divisor.
 * \return void
 *
 */
void dividir(float, float);

/** \brief La función recibe un numero, comprueba que sea un entero positivo,
 *         calcula su factorial y muestra el resultado.
 *
 * \param int recibe un numero entero
 * \return void
 *
 */
void factorial(float);

#endif // FUNCIONES_H_INCLUDED
